window.i18n = (function () {
  const DEFAULT_LANG = 'cs';
  const STORAGE_KEY = 'pf_lang';

  function getLang() {
    return localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG;
  }

  function t(key) {
    const lang = getLang();
    return (window.translations[lang] && window.translations[lang][key]) || key;
  }

  function applyTranslations(lang) {
    const dict = window.translations[lang];
    if (!dict) return;

    // Text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const val = dict[el.getAttribute('data-i18n')];
      if (val !== undefined) el.textContent = val;
    });

    // Inner HTML (for titles with nested gradient spans / <br>)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const val = dict[el.getAttribute('data-i18n-html')];
      if (val !== undefined) el.innerHTML = val;
    });

    // Placeholder attributes
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const val = dict[el.getAttribute('data-i18n-placeholder')];
      if (val !== undefined) el.placeholder = val;
    });

    // Update <html lang> attribute
    document.documentElement.lang = lang;

    // Update page <title> and meta description
    const page = document.documentElement.getAttribute('data-page');
    if (page) {
      const titleVal = dict['page.' + page + '.title'];
      const metaVal  = dict['page.' + page + '.meta'];
      if (titleVal) document.title = titleVal;
      const metaEl = document.querySelector('meta[name="description"]');
      if (metaEl && metaVal) metaEl.setAttribute('content', metaVal);
    }
  }

  function updateSwitcher(lang) {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
  }

  function setLang(lang) {
    localStorage.setItem(STORAGE_KEY, lang);
    applyTranslations(lang);
    updateSwitcher(lang);
  }

  function init() {
    const lang = getLang();
    applyTranslations(lang);
    updateSwitcher(lang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => setLang(btn.getAttribute('data-lang')));
    });
  }

  // Auto-init on DOM ready
  document.addEventListener('DOMContentLoaded', init);

  return { getLang, setLang, t };
})();
